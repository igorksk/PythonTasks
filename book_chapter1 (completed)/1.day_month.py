"""Task: Prompt the user for the current day and month and print them.

Prompts for the numeric day and the month name, then displays
the formatted result: "Today is <day> <month>".
"""

# Ask the user for the current day and month
day = input("Enter the current day (e.g., 28): ")
month = input("Enter the current month (e.g., June): ")

# Print the message
print(f"Today is {day} {month}.")